package L8_Methods;

public class Math_Builtin {
    public static void main(String[] args) {

        System.out.println(Math.sqrt(81));

        System.out.println(Math.cbrt(64));

        System.out.println(Math.abs(-98));

        System.out.println(Math.floor(4.9999));

        System.out.println(Math.ceil(4.0001));

        System.out.println(Math.min(5,9));

        System.out.println(Math.max(5,9));

        System.out.println(Math.pow(2,10));
    }
}
