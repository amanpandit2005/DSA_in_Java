package L1_L2_Basics;

public class Variables_Basics {
    public static void main(String[] args) {
        int x = 5;  //Declaration and Initialization
        System.out.println(x);
        System.out.println(x+5);
        int y;  //Declaration
        y = 20;  //Initialization
        System.out.println(y);
        int a = 5;  //Declaration and Initialization
        System.out.print(a+" ");
        int b;  //Declaration
        b = 20;  //Initialization
        System.out.println(b);

        // int x = 7;  We cannot declare same varible
        x = 7;
        System.out.println(x);
        System.out.println(x*5);
        x = x+3;
        System.out.println(x);

    }
}
