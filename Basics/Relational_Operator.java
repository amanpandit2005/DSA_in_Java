package L1_L2_Basics;

public class Relational_Operator {
    public static void main(String[] args) {
        System.out.println(5>9);
        System.out.println(5<9);
        System.out.println(5==9);
        System.out.println(9==9);
        System.out.println(5<=5);
        System.out.println(5>=5);
        System.out.println(5!=9);
    }
}
