package L20_L21_Strings;

public class String_to_Character_Array {
    public static void main(String[] args) {
        String s = "Aman Pandit";
        char[] arr =s.toCharArray();
        for(char ch : arr) {
            System.out.println(ch);
        }
        System.out.println(s);
    }
}

